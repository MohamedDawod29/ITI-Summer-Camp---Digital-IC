
x = 5
y = 3
########################### X and Y are integers##########################
A = x + y
print ("A = ",A)
print (type (A))

M = x * y
print ("M = ",M)
print (type (M))

########################### X and Y are floats##########################
A = float(x + y)
print ("A = ",A)
print (type (A))

M = float(x * y)
print ("M = ",M)
print (type (M))

########################### X and Y are strings##########################
A = str(x + y)
print ("A = ",A)
print (type (A))

M = str(str(x) * y)
print ("M = ",M)
print (type (M))

